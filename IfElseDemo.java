package com.anpc8323.day2;

public class IfElseDemo {

	public static void main(String[] args) {
		
		int number1 = 20;
		int number2 = 10;
		
		if(number1 <= number2)
		{
			System.out.println(number1 + " is lesser than or equal to " + number2);
		}
		else
		{
			System.out.println(number1 + " is greater than " + number2);
		}
	}

}
